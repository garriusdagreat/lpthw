tabby_cat = "\tI'm tabbed in."
persian_cat = "I'm split\non a line."
backslash_cat = "I'm \\ a \\ cat."

fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip \n\t* Grass
"""

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)

### ESCAPE SEQUENCES ###
# Examples:
# \t - indents or tabs in line
# \n - print on new line
# \\ - insert 1 slash into string
# \" - insert " (e.g: "5' 6\"") to tell python to differentiate double quote in string
# \' - insert ' (e.g: '5\' 6"') same as above but for single quote
